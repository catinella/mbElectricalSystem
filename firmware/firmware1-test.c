#include <mbesPinsMap.h>
#include <mbesUtitities.h>
#include <avr/io.h>


int main() {
	char    *pin = o_KEEPALIVE;
	
	CLKPR = (1 << CLKPCE);  // Clock prescaling enabling
	CLKPR = 0;              // It sets the prescaler to 1
	
	while (1) {
		setPinValue (pin, 1);
		_delay_ms(1000);
		setPinValue (pin, 0);
		_delay_ms(1000);
	}
	
	return(0);
}
